# Evaluate Random Forest
results.append(evaluate_model(regressor_rf, X_test, y_test, 'Random Forest Regression'))

# Evaluate SVM
results.append(evaluate_model(regressor_svm, X_test, y_test, 'Support Vector Machine (SVM)'))

# Evaluate KNN
